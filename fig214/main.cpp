//incremento y posincremento

#include <iostream>
using std::cout;
using std::cin;
using std::endl;

int main(){
	int c;
	c = 5;
	cout << c << endl;
	cout << c++ << endl;
	cout << c << endl << endl;

	c = 5;
	cout << c << endl;
	cout << ++c << endl;
	cout << c << endl;

	return 0;
}//place the enterteiner chords here ...
